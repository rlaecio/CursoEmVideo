s = n1 / n2
print('A divisão de {} e {} é igual a {}' .format(n1, n2, s))
s = n1 ** n2
print('A potencia de {} e {} é igual a {}' .format(n1, n2, s))
s = n1 // n2
print('A divisão inteira de {} e {} é igual a {}' .format(n1, n2, s))
s = n1 % n2
